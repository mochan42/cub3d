#include "../inc/libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*s_int;

	s_int = (char *)s;
	i = 0;
	while (i < (int)n)
	{
		*(s_int + i) = 0;
		i++;
	}
}
